-- 001_calendar_days.sql
-- Календар робочих/вихідних/святкових днів.
-- Таблиця зберігає ТІЛЬКИ винятки (свята, перенесені робочі суботи тощо).
-- Дефолт: Пн-Пт = workday, Сб-Нд = weekend. Calendar-сервіс це розуміє без запису.
-- 2026-04-11

CREATE TABLE IF NOT EXISTS `calendar_days` (
  `date`       DATE NOT NULL,
  `kind`       ENUM('workday','weekend','holiday','shortday') NOT NULL DEFAULT 'holiday',
  `title`      VARCHAR(256) DEFAULT NULL,
  `country`    CHAR(2) NOT NULL DEFAULT 'UA',
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`date`, `country`),
  KEY `idx_kind` (`kind`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

